package selectionstatemen.java.com;

public class Else_If {
	public static void main(String[] args) {
		int a=1; //initialization
		String s = "Starc";
		if (a==1) {  //== Check condition
			System.out.println(a);
		}
		else if (s=="Starc") {
			System.out.println(s);
		}
		else
		{
			System.out.println("Invalid");
	}
	}

}
